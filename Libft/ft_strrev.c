#include "libft.h"

char	*ft_strrev(char *str)
{
	int		start;
	int		end;
	char	temp;

	if (!str)
		return (NULL);
	start = 0;
	end = ft_strlen(str) - 1;
	while (start < end)
	{
		temp = str[start];
		str[start] = str[end];
		str[end] = temp;
		start++;
		end--;
	}
	return (str);
}